package org.example.wukongelement;
/**
 * @description: 自定义标签
 * @projectName spring
 * @author wukong
 * @date 2020/6/4 13:06
 */
public class User {
	private String id;
	private String name;
	private String email;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
